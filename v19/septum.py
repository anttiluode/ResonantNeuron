import numpy as np


def prc_Z(theta):
    """Phase response curve Z(theta): how strongly, and in which direction, a
    reset input shifts an oscillator's phase as a function of its current
    phase. Vardalakis use a PRC inspired by hippocampal pyramidal-cell PRCs
    (Lengyel 2005, Akam 2012): predominantly phase-ADVANCING, vanishing near
    the peak. A simple, faithful form is Z(theta) = -sin(theta): zero at the
    peak (theta=0), maximal mid-cycle, sign set so an input pulls the phase
    toward the peak (the reset target). This is the standard sinusoidal PRC of
    a system relaxing toward its cycle peak."""
    return -np.sin(theta)


class KuramotoSeptum:
    """The medial septum as a bank of Kuramoto oscillators producing a
    dynamical theta rhythm with phase reset. Vardalakis et al. 2024."""

    def __init__(self, n=50, f0=7.0, sigma=0.5, k=6.0, g_reset=25.0,
                 g_theta=1.0, dt=1e-3, seed=0):
        rng = np.random.default_rng(seed)
        self.n = int(n)
        self.dt = float(dt)                 # seconds per step (1 ms)
        self.f0 = float(f0)                 # center theta frequency (Hz)
        # natural angular frequencies, normal around 2*pi*f0
        self.omega = 2 * np.pi * rng.normal(f0, sigma, self.n)
        self.k = float(k)                   # coupling strength
        self.g_reset = float(g_reset)       # reset input gain
        self.g_theta = float(g_theta)       # output theta gain
        self.theta = rng.uniform(0, 2 * np.pi, self.n)  # initial phases
        self.A = 0.0                        # order-parameter amplitude (coherence)
        self.phi = 0.0                      # ensemble mean phase
        self.I_theta = 0.0                  # output theta drive (rectified cos)

    def order_parameter(self):
        r = np.mean(np.exp(1j * self.theta))
        return abs(r), np.angle(r)

    def step(self, reset_input=0.0):
        """Advance one dt. reset_input = X(t), the downstream firing fed back
        to the septum (0 if none). Returns the output theta drive I_theta."""
        A, phi = self.order_parameter()
        self.A, self.phi = A, phi
        # mean-field Kuramoto coupling: (k/N) sum_j sin(theta_j - theta_i)
        # = k * A * sin(phi - theta_i)   (exact mean-field identity)
        coupling = self.k * A * np.sin(phi - self.theta)
        reset = self.g_reset * float(reset_input) * prc_Z(self.theta)
        dtheta = self.omega + coupling + reset
        self.theta = (self.theta + self.dt * dtheta) % (2 * np.pi)
        # output theta: rectified cosine of the mean phase, scaled by coherence
        A2, phi2 = self.order_parameter()
        self.I_theta = self.g_theta * A2 * max(np.cos(phi2), 0.0)
        return self.I_theta

    # convenience reads
    def theta_phase(self):
        return self.phi

    def coherence(self):
        return self.A


