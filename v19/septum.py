if __name__ == "__main__":
    import numpy as np

    print("=" * 70)
    print("KURAMOTO SEPTUM -- does the oscillator bank generate a theta rhythm?")
    print("=" * 70)
    sep = KuramotoSeptum(n=50, f0=7.0, k=6.0, dt=1e-3, seed=1)
    fs = 1000.0  # 1 kHz (dt=1ms)
    T = 2.0
    out = np.array([sep.step(0.0) for _ in range(int(T * fs))])

    # dominant frequency of the generated rhythm
    sig = out - out.mean()
    freqs = np.fft.rfftfreq(len(sig), 1 / fs)
    P = np.abs(np.fft.rfft(sig)) ** 2
    f_peak = freqs[1 + np.argmax(P[1:])]
    print(f"  generated rhythm peak frequency: {f_peak:.2f} Hz "
          f"(target theta f0 = 7 Hz)")
    print(f"  final coherence (order parameter A): {sep.coherence():.3f} "
          f"(0=incoherent, 1=fully synchronized)")
    print(f"  -> {'PASS: theta-band rhythm self-organizes' if 4 <= f_peak <= 12 else 'FAIL'}")

    print("\n" + "=" * 70)
    print("PHASE RESET -- does a pulse re-align the clock to a defined phase?")
    print("=" * 70)
    sep = KuramotoSeptum(n=50, f0=7.0, k=6.0, dt=1e-3, seed=2)
    # run two identical septa; pulse one mid-run, compare phases after
    sep2 = KuramotoSeptum(n=50, f0=7.0, k=6.0, dt=1e-3, seed=2)
    phases_unpulsed = []
    phases_pulsed = []
    for t in range(1000):
        sep.step(0.0)
        # deliver a strong reset pulse to sep2 at t=500 for 5 ms
        rin = 1.0 if 500 <= t < 505 else 0.0
        sep2.step(rin)
        phases_unpulsed.append(sep.theta_phase())
        phases_pulsed.append(sep2.theta_phase())
    # phase difference well after the pulse
    dphi = np.angle(np.exp(1j * (np.array(phases_pulsed[-200:]) -
                                 np.array(phases_unpulsed[-200:]))))
    print(f"  mean phase shift induced by the pulse: {np.mean(dphi):+.3f} rad "
          f"({np.degrees(np.mean(dphi)):+.0f} deg)")
    print(f"  -> {'PASS: the pulse measurably reset the theta phase' if abs(np.mean(dphi)) > 0.05 else 'no reset detected'}")
    print("\nThe septum self-organizes a theta rhythm from a bank of edge-poised")
    print("oscillators, and a salient input resets its phase -- exactly the")
    print("pacemaker the resonator neuron's latency findings kept demanding.")
